int main()
{
	//변수 선언
	Player p1; //플레이어 생성
	char ch; //키입력 변수
	int oldHP, oldEXP, oldAmmo, oldStimpack; //각종 정보 표시를 위한 변수
	int left[5] = { 4,4,5,5,1 };
	srand(time(NULL)); 	//난수 발생
	//인트로
	intro();
	//캐릭 설정
	player_set(&p1);
	//처음 기본값 설정
	oldHP = p1.HP;
	oldEXP = p1.EXP;
	oldAmmo = p1.inventory.Ammo;
	oldStimpack = p1.inventory.Stimpack;
	//게임 시작
	firststage();
	stage(MAP1);
	player_ingame_info(&p1);
	//1스테이지 시작
	while (1)
	{
		if (kbhit() == 1)
		{
			ch = getch();
			switch (ch) {
				case UP:
				case DOWN:
				case LEFT:
				case RIGHT:
					if (i == 0) //1스테이지
					{
						player_move(&p1, ch, MAP1, LEFTMONSTERS, STAGENUMBER);
						if (left[i] != LEFTMONSTERS[i])
						{
							combat_main(i, &p1);
							left[i]--;
							islevelup(&p1);
							stage(MAP1);
						}
						break;
					} 
					if (i == 1) //2스테이지
					{
						player_move(&p1,ch, MAP2, LEFTMONSTERS,STAGENUMBER);
						if (left[i] != LEFTMONSTERS[i])
						{
							combat_main(i, &p1);
							left[i]--;
							islevelup(&p1);
							stage(MAP2);
						}
						break;
					}
					if (i == 2) //3스테이지
					{
						player_move(&p1, ch, MAP3, LEFTMONSTERS, STAGENUMBER);
						if (left[i] != LEFTMONSTERS[i])
						{
							combat_main(i, &p1);
							left[i]--;
							islevelup(&p1);
							stage(MAP3);
						}
						break;
					}
					if (i == 3) //4스테이지
					{
						player_move(&p1, ch, MAP4, LEFTMONSTERS, STAGENUMBER);
						if (left[i] != LEFTMONSTERS[i])
						{
							combat_main(i, &p1);
							left[i]--;
							islevelup(&p1);
							stage(MAP4);
						}
						break;
					}
					if (i == 4) //5스테이지
					{
						player_move(&p1, ch, MAP5, LEFTMONSTERS, STAGENUMBER);
						if (left[i] != LEFTMONSTERS[i])
						{
							combat_main(i, &p1);
							left[i]--;
							islevelup(&p1);
							stage(MAP5);
						}
						break;
					}
				case STIMPACK:
					player_stimpack(&p1);
					break;
				//case 'z':
				//	p1.HP -= 15;
				//	break;
				default:
					break;
			}
		}
		if (LEFTMONSTERS[i] == 0)
		{
			if (i == 0) {
				nextstage(i); stage(MAP2);
				player_ingame_info(&p1);
				i++; STAGENUMBER++;
				continue;
			}
			if (i == 1) {
				nextstage(i); stage(MAP3);
				player_ingame_info(&p1);
				i++; STAGENUMBER++;
				continue;
			}
			if (i == 2) {
				nextstage(i); stage(MAP4);
				player_ingame_info(&p1);
				i++; STAGENUMBER++;
				continue;
			}
			else if (i == 3) {
				nextstage(i); stage(MAP5);
				player_ingame_info(&p1);
				i++; STAGENUMBER++;
				continue;
			}
			else if (i == 4)
			{
				if (p1.HP > 0)
				{
					nextstage(i);
					ending();
				}
				break;
			}
			else
			{
				continue;
			}
		}
		if (oldHP != p1.HP || oldEXP != p1.EXP || oldAmmo != p1.inventory.Ammo||oldStimpack !=
			p1.inventory.Stimpack)
		{
			player_ingame_info(&p1);
		}
		if (p1.HP <= 0)
		{
			textcolor(WHITE, RED);
			gotoxy(5, 10);
			printf("\a당신은 죽었습니다. 볼트 13은 또 다른 거주자에게 임무를 줄 것 입니다.");
			getch();
			break;
		}
	}
	return 0;
}